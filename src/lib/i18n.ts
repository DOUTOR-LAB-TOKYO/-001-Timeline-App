// eslint-disable-next-line @typescript-eslint/no-explicit-any
type TranslationMap = { [K in TranslationKey]: typeof ja[K] extends (...args: any[]) => string ? typeof ja[K] : string };
export const translations: Record<Language, TranslationMap> = { ja, en, ko, zh, es, fr, pt };

export type TranslationKey = keyof typeof ja;

export function useT() {
  const language = useAppStore((s) => s.language);
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  return <K extends TranslationKey>(key: K): typeof ja[K] => translations[language][key] as any;
}

// For use outside React (event handlers, log messages)
export function getT(language: Language) {
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  return <K extends TranslationKey>(key: K): typeof ja[K] => translations[language][key] as any;
}
